    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
            int dem = 0;
            TaiKhoan tk = new TaiKhoan();
            tk.setId(getTxtId());
            tk.setTenDangNhap(getTxtTenDangNhap());
            tk.setMatKhau(getTxtMatKhau());
            tk.setVaiTro("user");
            for(TaiKhoan i:dstk){
                if(tk.getTenDangNhap().equals(i.getTenDangNhap()) || tk.getId().equals(i.getId())){
                    dem++;
                }
            }
            if(dem==0){
                Clear();
                dstk.add(tk);
                loadTable(dstk);
            }else{
                throw new Exception("Tên đăng nhập hoặc id đã tồn tại");
            }
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(this, e.toString(),"Thông báo",JOptionPane.OK_CANCEL_OPTION,JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_btnThemActionPerformed

