    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        // TODO add your handling code here:
        try {
            int dem = 0;
            TaiKhoan tk = new TaiKhoan();
            tk.setId(getTxtId());
            tk.setTenDangNhap(getTxtTenDangNhap());
            for(TaiKhoan i:dstk){
                if(tk.getTenDangNhap().equals(i.getTenDangNhap()) || tk.getId().equals(i.getId())){
                    Clear();
                    tk.setId(i.getId());
                    tk.setTenDangNhap(i.getTenDangNhap());
                    tk.setMatKhau(i.getMatKhau());
                    tk.setVaiTro(i.getVaiTro());
                    dstk = new ArrayList<>();
                    dstk.add(i);
                    Clear();
                    tbTaiKhoan.setModel(new TableTK(dstk));
                    dem++;
                }
            }
            if(dem==0) {
                throw new Exception("Không tìm thấy id hoặc tên đăng nhập");
            }
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(this, e.toString(),"Thông báo",JOptionPane.OK_CANCEL_OPTION,JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnTimActionPerformed

